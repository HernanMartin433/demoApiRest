using Microsoft.EntityFrameworkCore;
using Microsoft.EntityFrameworkCore.Metadata.Builders;
using MiProducto.Domain.Entities;

namespace MiProducto.Infrastructure.Configuration;

public class SubrubroConfiguration : IEntityTypeConfiguration<Subrubro>
{
    public void Configure(EntityTypeBuilder<Subrubro> builder)
    {
        builder.HasKey(s => s.Id);
        builder.Property(s => s.Name).IsRequired().HasMaxLength(200);
        builder.Property(s => s.IsActive).IsRequired();
    }
}